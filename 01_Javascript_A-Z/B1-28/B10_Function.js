// Function (hàm) in JS
function calculateTriangleSquare(a, h) {
    return a * h / 2
}

// Tính diện tích tam giác có chiều cao
// h = 5, cạnh đáy a = 10 
calculateTriangleSquare(10, 5);

// Tính diện tích hình trìn r = 10
function calculateDiscSquare(r) {
    return r * r * 3.14
}
calculateDiscSquare(10);




